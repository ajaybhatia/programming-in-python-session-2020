# Arithmetic Operators
# Additon: +
# Multiplication: * (Asterisk)
# Division: /
# Integer Division: //
# Exponetiation: **
# Subtraction: -
# Modulus (Reminder): %


x, y = 10, 3

# Addition
z = x + y
print(f"{x} + {y} = {z}")  # printf("%d + %d = %d", x, y, z);

# Subtraction
z = x - y
print(f"{x} - {y} = {z}")

# Multiplication
z = x * y
print(f"{x} X {y} = {z}")

# Division
z = x / y
print(f"{x} / {y} = {z}")

# Integer Division
z = x // y
print(f"{x} / {y} = {z}")

# Modulud
z = x % y
print(f"{x} % {y} = {z}")

# Exponentation
z = x ** y
print(f"{x} raise to power {y} = {z}")
