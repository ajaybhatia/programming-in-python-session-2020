# Variable (identifier) Naming rules
# 1. Name should start with either an alphabet or an underscore
# 2. There should be no whitespace in name
# 3. There can be alphabets, underscores or digits in a name
# 4. Consecutive underscores are allowed

x, y, z = 1, 2, 3
print(x, y, z)

x = 3.14
print(type(x), type(y), type(z))
